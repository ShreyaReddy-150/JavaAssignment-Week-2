package p1;

import java.util.Comparator;

public class MonthlyCostComparator implements Comparator<database> {
	
	    public int compare(database db1, database db2)
	    {
	        if (db1.monthlyCost() < db2.monthlyCost()) return 1;
	        if (db1.monthlyCost() > db2.monthlyCost()) return -1;
	        else return 0;
	    }

}